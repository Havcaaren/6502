void toBin(bool *b, int num) {
  for (int i = 0; i < 13; i++) {
    b[i] = false;
  }
  int a = 8192;
  for (int i = 12; i >= 0; i++) {
    if (num - a >= 0) {
      b[i] = true;
      num -= a;
    }
    a /= 2;
  }
}

void setup() {
  Serial.begin(9600);
  pinMode(2, OUTPUT);
  for (int i = 0; i < 13; i++) {
    pinMode(22 + i, OUTPUT);
  }
  for (int i = 0; i < 8; i++) {
    pinMode(3 + i, OUTPUT);
  }
  for (int i = 0; i < 13; i++) {
    digitalWrite(22 + i, LOW);
  }
  digitalWrite(2, OUTPUT);
  
  bool add[13] = {0};
  int b = 0;
  for (int i = 64; i < 8192; i += 64) {
    for (int a = 0; a < 16; a++) {
      toBin(add, (i + a));
      Serial.print(i+a);
      Serial.print("\t:");
      for (int j = 0; j < 13; j++) {
        digitalWrite(22 + j, add[j]);
        Serial.print(add[j]);
      }
      Serial.print("V: ");
      for (int j = 0; j < 8; j++) {
        digitalWrite(3 + j,X0[a][j]);
        Serial.print(X0[b][j]);
      }
      b++;
      Serial.println(" ");
      delay(10);
    }
  }
}

